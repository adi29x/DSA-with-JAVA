public class LinearTraversal {
    public static void main (String[] args) {
        int[] arr = {10, 20, 30, 40, 50};
        int size = arr.length;
        System.out.println("Elements in the array are:");
        for (int i = 0; i < size; i++) {
            System.out.println(arr[i] + " ");
        }
    }
}
